"""DP 问题特性练习版：理解动态规划的核心要素与状态扩展技巧。


动态规划三大核心特性：
1. **最优子结构** (Optimal Substructure)
   - 大问题的最优解可以通过组合子问题的最优解得到
   - 例如：最短路径问题中，A→C的最短路径 = A→B的最短路径 + B→C的最短路径

2. **重叠子问题** (Overlapping Subproblems)
   - 递归求解时，相同的子问题会被多次计算
   - 解决方案：使用记忆化搜索或DP表缓存结果
   - 例如：斐波那契数列 f(5) 需要计算 f(3) 两次

3. **无后效性** (No Aftereffect)
   - 一旦某个状态确定，之后的发展只与当前状态有关
   - 与之前如何到达该状态无关
   - 例如：爬楼梯到第 i 阶，只关心"在第 i 阶"，不关心怎么来的


本练习包含两个变体问题：
- 问题1：最小代价爬楼梯（带权重的路径优化）
- 问题2：带约束的爬楼梯（状态扩展示例）
"""


def min_cost_climbing_stairs_dp(cost: list[int]) -> int:
    """最小代价爬楼梯：动态规划实现。

    问题描述：
    数组 cost 的第 i 个数字表示从第 i 阶台阶向上爬需要支付的费用。
    一旦支付费用，可以选择向上爬 1 或 2 个台阶。
    你需要找到达到楼层顶部的最低花费。

    示例：
    输入: cost = [0, 1, 10, 2, 7, 3]
    解释: 从第2阶开始（cost[2]=10），然后走两步到第4阶（cost[4]=7），再走两步到楼顶
         总花费: 10 + 7 = 17

    核心思路：
    - 定义 dp[i] 为到达第 i 阶台阶的最小累计花费
    - 到达第 i 阶有两种方式：
      * 从第 i-1 阶走 1 步上来（花费 dp[i-1] + cost[i]）
      * 从第 i-2 阶走 2 步上来（花费 dp[i-2] + cost[i]）
    - 取两者中的较小值
    """
    # 步骤 1：获取最高台阶编号
    # TODO: 获取数组长度减1，赋值给 n（因为cost[0]是地面）
    n = len(cost) - 1

    # 步骤 2：处理边界情况
    # TODO 1: 若 n <= 0，返回 0（没有台阶或只有地面）
    if n <= 0:
        return 0

    # TODO 2: 若 n == 1 或 n == 2，直接返回 cost[n]（只能直接到达）
    if n == 1 or n == 2:
        return cost[n]

    # 步骤 3：创建 dp 数组
    # TODO: 创建长度为 n+1 的列表，初始值全为 0
    dp = [0] * (n + 1)

    # 步骤 4：初始化已知条件
    # TODO: 设置 dp[1] 和 dp[2] 的初始值（直接使用 cost[1] 和 cost[2]）
    dp[1], dp[2] = cost[1], cost[2]

    # 步骤 5：填充 dp 表
    # TODO: 使用 for 循环遍历 i 从 3 到 n
    for i in range(3, n + 1):

        # 步骤 6：应用状态转移方程
        # TODO: dp[i] = min(dp[i-1], dp[i-2]) + cost[i]
        # 解释：取从左边来还是从左边隔一个位置来，哪个花费更小
        dp[i] = min(dp[i - 1], dp[i - 2]) + cost[i]

    # 步骤 7：返回最终结果
    # TODO: 返回 dp[n]，即到达最高台阶的最小花费
    return dp[n]


def climbing_stairs_no_consecutive_one_step(n: int) -> int:
    """带约束的爬楼梯：不能连续两次走 1 步。

    问题描述升级：
    在普通爬楼梯的基础上增加约束条件：
    - 每次可以走 1 或 2 步
    - 但不能连续两轮都走 1 步

    为什么需要状态扩展？
    - 普通DP只记录"到达第i阶的方法数"
    - 但现在需要知道"最后一步是怎么走的"
    - 如果最后一步走了1步，下一步就不能再走1步了！

    状态扩展技巧：
    - 将一维状态 dp[i] 扩展为二维状态 dp[i][j]
    - j 表示最后一步走的步数：
      * j=0: 最后一步走的是 1 步
      * j=1: 最后一步走的是 2 步
    """
    # 步骤 1：处理边界情况
    # TODO 1: 若 n < 0，返回 0（无效输入）
    # TODO 2: 若 n == 0，返回 1（在地面，算作一种情况）
    if n < 0:
        return 0
    if n == 0:
        return 1

    # 步骤 2：创建二维 dp 表
    # TODO: 创建大小为 (n+1) × 2 的二维列表，初始值全为 0
    # 含义：
    #   dp[i][0]: 到达第 i 阶且最后一步是 1 步的方法数
    #   dp[i][1]: 到达第 i 阶且最后一步是 2 步的方法数
    dp = [[0, 0] for _ in range(n + 1)]

    # 步骤 3：初始化基础情况
    # TODO 1: 若 n >= 1，设置 dp[1][0] = 1
    #       解释：到达第1阶只能走1步（走2步会超过），所以最后一步一定是1步
    if n >= 1:
        dp[1][0] = 1

    # TODO 2: 若 n >= 2，设置 dp[2][1] = 1
    #       解释：到达第2阶如果走1步，那上一步也是1步（违反约束！）
    #             所以只能走2步从地面直接过来，最后一步是2步
    if n >= 2:
        dp[2][1] = 1

    # 步骤 4：状态转移（关键！理解约束如何体现）
    # TODO: 使用 for 循环遍历 i 从 3 到 n
    for i in range(3, n + 1):

        # 步骤 5：处理最后一步是 1 步的情况
        # TODO: dp[i][0] = dp[i-1][1]
        # 解释：
        #   如果最后一步要走 1 步到达第 i 阶，
        #   那么上一步（第 i-1 阶）的最后一步必须是 2 步，
        #   否则就会出现连续两个 1 步，违反约束！
        dp[i][0] = dp[i - 1][1]

        # 步骤 6：处理最后一步是 2 步的情况
        # TODO: 如果 i >= 2，则 dp[i][1] = dp[i-2][0] + dp[i-2][1]
        # 解释：
        #   如果最后一步走 2 步到达第 i 阶，
        #   说明是从第 i-2 阶过来的（不管第 i-2 阶最后一步是什么都可以）
        #   因为中间跨了 2 步，不会产生连续 1 步的问题
        if i - 2 >= 0:
            dp[i][1] = dp[i - 2][0] + dp[i - 2][1]

    # 步骤 7：返回最终结果
    # TODO: 返回 dp[n][0] + dp[n][1]
    # 解释：总方法数 = 最后一步走1步的方法数 + 最后一步走2步的方法数
    return dp[n][0] + dp[n][1]


