"""最大容量问题（LeetCode 11 - 盛最多水的容器）。

问题描述：
给定一个整数数组 height，每个元素代表一个垂直隔板的高度。
选择两个隔板使得它们与x轴组成的容器容量最大。
容器容量 = min(高度1, 高度2) × 宽度（索引之差）

贪心策略：
使用双指针从两端向中间收缩。
每次移动较短的指针（因为移动短板才可能增大面积）。

时间复杂度：O(n)
空间复杂度：O(1)
"""


def max_capacity(ht: list[int]) -> int:
    """计算容器的最大容量。

    算法思路：
    1. 使用双指针 i, j 分别指向数组首尾
    2. 计算当前指针形成的容器容量
    3. 移动高度较小的指针（关键！）
    4. 重复直到两指针相遇

    为什么移动短板？
    - 容量由较短的高度决定
    - 移动长板不会增加高度（受限于短板）
    - 移动短板可能遇到更高的板，从而增加容量

    参数：
        ht: 隔板高度数组

    返回：
        最大容量值
    """
    # 初始化双指针，分别指向数组两端
    i, j = 0, len(ht) - 1

    # 初始化最大容量为 0
    res = 0

    # 循环贪心选择，直至两指针相遇
    while i < j:

        # 计算当前容器的容量
        # 容量 = min(左板高, 右板高) × 宽度
        cap = min(ht[i], ht[j]) * (j - i)

        # 更新最大容量
        res = max(res, cap)

        # 向内移动短板（贪心核心）
        if ht[i] < ht[j]:
            i += 1  # 左边更短，向右移动左指针
        else:
            j -= 1  # 右边更短或相等，向左移动右指针

    return res


