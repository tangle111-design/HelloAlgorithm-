"""最大切分乘积问题练习版：整数拆分的数学优化问题。


问题描述：
给定一个正整数 n（n ≥ 2），
将其拆分为至少两个正整数的和，
使得这些整数的乘积最大化。
返回最大的乘积值。

示例分析：
n=2: 拆为 1+1, 乘积 = 1×1 = 1
n=3: 拆为 1+2, 乘积 = 1×2 = 2
n=4: 拆为 2+2, 乘积 = 2×2 = 4 (比 3+1 的 3×1=3 更好)
n=5: 拆为 3+2, 乘积 = 3×2 = 6
n=10: 拆为 3+3+2+2, 乘积 = 3×3×2×2 = 36


数学原理（为什么优先拆成 3？）：
对于任意正整数 x ≥ 5，都有：
- 将 x 拆分成 3 和 x-3，乘积为 3(x-3) = 3x-9
- 这个结果大于 x 本身（因为 3x-9 > x 当 x > 4.5）

所以最优策略是尽可能多地拆出 3！

特殊情况处理：
- 余数为 0: 全部拆成 3
- 余数为 1: 把一个 3+1 改成 2+2（因为 2×2 > 3×1）
- 余数为 2: 保留这个 2
"""

import math


def max_product_cutting(n: int) -> int:
    """计算将 n 拆分后的最大乘积。

    算法步骤：
    1. 处理 n ≤ 3 的特殊情况（必须拆出一个 1）
    2. 计算 n 中包含多少个 3 和余数
    3. 根据余数决定最终方案

    参数说明：
        n: 要拆分的正整数（n ≥ 2）

    返回值：
        拆分后能得到的最大乘积
    """
    # 步骤 1：处理边界情况（n ≤ 3 时特殊处理）
    # TODO: 若 n <= 3，返回 1 * (n - 1)
    # 解释：当 n<=3 时，必须至少拆成两部分，
    #       所以拆成 1 和 n-1，乘积就是 1*(n-1)
    if n <= 3:
        return 1 * (n - 1)

    # 步骤 2：计算可以拆分出多少个 3 以及余数
    # TODO: 使用 divmod 函数计算 a, b = divmod(n, 3)
    # 含义：
    #   - a: 可以完整拆出的 3 的个数
    #   - b: 剩余的余数（只能是 0、1 或 2）
    a, b = divmod(n, 3)

    # 步骤 3：根据余数决定如何处理
    # TODO: 使用 if-elif-else 结构处理三种情况：

    if b == 0:
        # 情况1 - 余数为 0
        # TODO: 返回 3^a（全部拆成 3）
        # 提示：使用 math.pow(3, a) 并转换为 int
        return int(math.pow(3, a))

    elif b == 1:
        # 情况2 - 余数为 1（关键！需要特殊处理）
        # TODO: 返回 3^(a-1) × 2 × 2
        # 解释：因为 3+1=4，而 4 拆成 2+2 更优（2×2 > 3×1）
        #       所以少用一个 3，把 3+1 转换成 2+2
        return int(math.pow(3, a - 1)) * 2 * 2

    else:  # b == 2
        # 情况3 - 余数为 2
        # TODO: 返回 3^a × 2（直接保留这个 2 即可）
        return int(math.pow(3, a)) * 2


